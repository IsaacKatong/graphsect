#!/usr/bin/env node
import { mkdirSync, writeFileSync } from "node:fs";
import { fileURLToPath } from "node:url";
import { dirname, join } from "node:path";

const here = dirname(fileURLToPath(import.meta.url));
const outPath = join(here, "..", "examples", "advanced-graph.json");
const imagesDir = join(here, "..", "public", "example-images");

// Deterministic PRNG (mulberry32) for reproducibility
let seed = 0xc0ffee;
function rand() {
  seed |= 0;
  seed = (seed + 0x6d2b79f5) | 0;
  let t = Math.imul(seed ^ (seed >>> 15), 1 | seed);
  t = (t + Math.imul(t ^ (t >>> 7), 61 | t)) ^ t;
  return ((t ^ (t >>> 14)) >>> 0) / 4294967296;
}
const randInt = (min, max) => Math.floor(rand() * (max - min + 1)) + min;
const pickK = (arr, k) => {
  const copy = arr.slice();
  const picked = [];
  for (let i = 0; i < k && copy.length; i++) {
    const j = Math.floor(rand() * copy.length);
    picked.push(copy.splice(j, 1)[0]);
  }
  return picked;
};

const N = 100;
const MAIN_DATUM_TAGS = Array.from(
  { length: 10 },
  (_, i) => `mainDatumTag${i}`,
);
const SUB_DATUM_TAGS = Object.fromEntries(
  MAIN_DATUM_TAGS.map((main, i) => [
    main,
    Array.from({ length: 10 }, (_, j) => `subDatumTag${i}-${j}`),
  ]),
);
const EDGE_TAGS = Array.from({ length: 10 }, (_, i) => `edgeTag${i}`);
const DIMENSIONS = [
  "importance",
  "complexity",
  "urgency",
  "novelty",
  "confidence",
  "effort",
  "risk",
  "impact",
  "clarity",
  "priority",
];

const makeId = (n) =>
  `a1b2c3d4-${String(n).padStart(4, "0")}-4000-8000-${String(n).padStart(12, "0")}`;

const ASPECT_RATIOS = [
  { label: "1x1", width: 480, height: 480 },
  { label: "16x9", width: 640, height: 360 },
  { label: "9x16", width: 360, height: 640 },
  { label: "4x3", width: 600, height: 450 },
  { label: "3x4", width: 450, height: 600 },
  { label: "21x9", width: 840, height: 360 },
  { label: "2x1", width: 600, height: 300 },
  { label: "1x2", width: 300, height: 600 },
  { label: "5x4", width: 600, height: 480 },
  { label: "3x2", width: 600, height: 400 },
];
const PALETTE = [
  ["#0ea5e9", "#1e3a8a"],
  ["#f97316", "#7c2d12"],
  ["#22c55e", "#14532d"],
  ["#a855f7", "#581c87"],
  ["#ef4444", "#7f1d1d"],
  ["#eab308", "#713f12"],
  ["#06b6d4", "#164e63"],
  ["#ec4899", "#831843"],
  ["#84cc16", "#365314"],
  ["#f59e0b", "#78350f"],
];

function makeSvg({ width, height, label, index, fg, bg }) {
  const fontSize = Math.round(Math.min(width, height) * 0.12);
  return `<?xml version="1.0" encoding="UTF-8"?>
<svg xmlns="http://www.w3.org/2000/svg" viewBox="0 0 ${width} ${height}" width="${width}" height="${height}">
  <defs>
    <linearGradient id="g" x1="0" y1="0" x2="1" y2="1">
      <stop offset="0%" stop-color="${fg}"/>
      <stop offset="100%" stop-color="${bg}"/>
    </linearGradient>
  </defs>
  <rect width="100%" height="100%" fill="url(#g)"/>
  <rect x="8" y="8" width="${width - 16}" height="${height - 16}" fill="none" stroke="rgba(255,255,255,0.4)" stroke-width="4"/>
  <text x="50%" y="48%" text-anchor="middle" dominant-baseline="middle" font-family="system-ui, sans-serif" font-weight="700" font-size="${fontSize}" fill="white">${label}</text>
  <text x="50%" y="68%" text-anchor="middle" dominant-baseline="middle" font-family="system-ui, sans-serif" font-size="${Math.round(fontSize * 0.5)}" fill="rgba(255,255,255,0.85)">image #${index} • ${width}×${height}</text>
</svg>
`;
}

mkdirSync(imagesDir, { recursive: true });
const imageSpecs = ASPECT_RATIOS.map((ratio, i) => {
  const [fg, bg] = PALETTE[i % PALETTE.length];
  const filename = `aspect-${ratio.label}.svg`;
  writeFileSync(
    join(imagesDir, filename),
    makeSvg({ ...ratio, index: i + 1, fg, bg }),
  );
  return {
    url: `example-images/${filename}`,
    label: ratio.label,
    width: ratio.width,
    height: ratio.height,
  };
});

const IMAGE_DATUM_COUNT = 10;
const imageDatumStride = Math.floor(N / IMAGE_DATUM_COUNT);
const imageDatumIndexes = new Set(
  Array.from({ length: IMAGE_DATUM_COUNT }, (_, k) => (k + 1) * imageDatumStride),
);

const datums = [];
let imageDatumOrdinal = 0;
for (let i = 1; i <= N; i++) {
  if (imageDatumIndexes.has(i)) {
    const spec = imageSpecs[imageDatumOrdinal % imageSpecs.length];
    imageDatumOrdinal++;
    const content = {
      imageUrl: spec.url,
      text: `# Image Note ${i}\n\nThis datum bundles an image with **markdown** text.\n\n- Aspect ratio: \`${spec.label}\`\n- Pixel size: ${spec.width}×${spec.height}\n- Datum index: ${i} of ${N}\n`,
    };
    datums.push({
      id: makeId(i),
      name: `Image Note ${i}`,
      type: "IMAGE_WITH_TEXT",
      content: JSON.stringify(content),
    });
  } else {
    datums.push({
      id: makeId(i),
      name: `Note ${i}`,
      type: "MARKDOWN",
      content: `# Note ${i}\n\nAutogenerated datum **${i}** of ${N}.`,
    });
  }
}

// creationTime range: last week (2026-04-11) to today (2026-04-18) UTC
const TODAY = Date.UTC(2026, 3, 18);
const LAST_WEEK = Date.UTC(2026, 3, 11);

const datumTags = [];
const datumDimensions = [];
const tagsByDatum = new Map();
for (const d of datums) {
  const k = randInt(1, 5);
  const mainTags = pickK(MAIN_DATUM_TAGS, k);
  const tags = [...mainTags];
  for (const main of mainTags) {
    const subCount = randInt(2, 5);
    tags.push(...pickK(SUB_DATUM_TAGS[main], subCount));
  }
  tagsByDatum.set(d.id, tags);
  for (const tag of tags) {
    datumTags.push({ name: tag, datumID: d.id });
  }

  const creation = LAST_WEEK + Math.floor(rand() * (TODAY - LAST_WEEK));
  datumDimensions.push({ name: "creationTime", datumID: d.id, value: creation });

  const dimCount = randInt(2, 5);
  for (const dim of pickK(DIMENSIONS, dimCount)) {
    datumDimensions.push({
      name: dim,
      datumID: d.id,
      value: randInt(1, 10),
    });
  }
}

// Edges: each datum gets up to 3 outgoing edges, only to datums sharing at least one tag
const edgeSet = new Set();
const edges = [];
for (const d of datums) {
  const myTags = new Set(tagsByDatum.get(d.id) ?? []);
  if (myTags.size === 0) continue;
  const candidates = datums.filter(
    (o) =>
      o.id !== d.id &&
      (tagsByDatum.get(o.id) ?? []).some((t) => myTags.has(t)),
  );
  const targetCount = Math.min(randInt(1, 3), candidates.length);
  for (const target of pickK(candidates, targetCount)) {
    const key = `${d.id}->${target.id}`;
    if (edgeSet.has(key)) continue;
    edgeSet.add(key);
    edges.push({ fromDatumID: d.id, toDatumID: target.id });
  }
}

const edgeTags = [];
for (const e of edges) {
  const k = randInt(1, 5);
  const edgeID = `${e.fromDatumID}->${e.toDatumID}`;
  for (const tag of pickK(EDGE_TAGS, k)) {
    edgeTags.push({ name: tag, edgeID });
  }
}

const graph = {
  datums,
  edges,
  datumTags,
  datumDimensions,
  datumTagAssociations: [],
  edgeTags,
  version: 0,
};

writeFileSync(outPath, JSON.stringify(graph, null, 2) + "\n");
const imageDatumCount = datums.filter((d) => d.type === "IMAGE_WITH_TEXT").length;
console.log(
  `Wrote ${outPath}: ${datums.length} datums (${imageDatumCount} IMAGE_WITH_TEXT), ${edges.length} edges, ${datumTags.length} datum tags, ${edgeTags.length} edge tags, ${datumDimensions.length} dimension values.`,
);
console.log(`Wrote ${imageSpecs.length} SVG images to ${imagesDir}`);
