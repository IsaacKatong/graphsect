#!/usr/bin/env node
import { writeFileSync } from "node:fs";
import { fileURLToPath } from "node:url";
import { dirname, join } from "node:path";

const here = dirname(fileURLToPath(import.meta.url));
const outPath = join(here, "..", "examples", "advanced-graph.json");

// Deterministic PRNG (mulberry32) for reproducibility
let seed = 0xc0ffee;
function rand() {
  seed |= 0;
  seed = (seed + 0x6d2b79f5) | 0;
  let t = Math.imul(seed ^ (seed >>> 15), 1 | seed);
  t = (t + Math.imul(t ^ (t >>> 7), 61 | t)) ^ t;
  return ((t ^ (t >>> 14)) >>> 0) / 4294967296;
}
const randInt = (min, max) => Math.floor(rand() * (max - min + 1)) + min;
const pickK = (arr, k) => {
  const copy = arr.slice();
  const picked = [];
  for (let i = 0; i < k && copy.length; i++) {
    const j = Math.floor(rand() * copy.length);
    picked.push(copy.splice(j, 1)[0]);
  }
  return picked;
};

const N = 100;
const DATUM_TAGS = Array.from({ length: 10 }, (_, i) => `datumTag${i + 1}`);
const EDGE_TAGS = Array.from({ length: 10 }, (_, i) => `edgeTag${i + 1}`);
const DIMENSIONS = [
  "importance",
  "complexity",
  "urgency",
  "novelty",
  "confidence",
  "effort",
  "risk",
  "impact",
  "clarity",
  "priority",
];

const makeId = (n) =>
  `a1b2c3d4-${String(n).padStart(4, "0")}-4000-8000-${String(n).padStart(12, "0")}`;

const datums = [];
for (let i = 1; i <= N; i++) {
  datums.push({
    id: makeId(i),
    name: `Note ${i}`,
    type: "MARKDOWN",
    content: `# Note ${i}\n\nAutogenerated datum **${i}** of ${N}.`,
  });
}

// creationTime range: last week (2026-04-11) to today (2026-04-18) UTC
const TODAY = Date.UTC(2026, 3, 18);
const LAST_WEEK = Date.UTC(2026, 3, 11);

const datumTags = [];
const datumDimensions = [];
const tagsByDatum = new Map();
for (const d of datums) {
  const k = randInt(1, 5);
  const tags = pickK(DATUM_TAGS, k);
  tagsByDatum.set(d.id, tags);
  for (const tag of tags) {
    datumTags.push({ name: tag, datumID: d.id });
  }

  const creation = LAST_WEEK + Math.floor(rand() * (TODAY - LAST_WEEK));
  datumDimensions.push({ name: "creationTime", datumID: d.id, value: creation });

  const dimCount = randInt(2, 5);
  for (const dim of pickK(DIMENSIONS, dimCount)) {
    datumDimensions.push({
      name: dim,
      datumID: d.id,
      value: randInt(1, 10),
    });
  }
}

// Edges: each datum gets up to 3 outgoing edges, only to datums sharing at least one tag
const edgeSet = new Set();
const edges = [];
for (const d of datums) {
  const myTags = new Set(tagsByDatum.get(d.id) ?? []);
  if (myTags.size === 0) continue;
  const candidates = datums.filter(
    (o) =>
      o.id !== d.id &&
      (tagsByDatum.get(o.id) ?? []).some((t) => myTags.has(t)),
  );
  const targetCount = Math.min(randInt(1, 3), candidates.length);
  for (const target of pickK(candidates, targetCount)) {
    const key = `${d.id}->${target.id}`;
    if (edgeSet.has(key)) continue;
    edgeSet.add(key);
    edges.push({ fromDatumID: d.id, toDatumID: target.id });
  }
}

const edgeTags = [];
for (const e of edges) {
  const k = randInt(1, 5);
  const edgeID = `${e.fromDatumID}->${e.toDatumID}`;
  for (const tag of pickK(EDGE_TAGS, k)) {
    edgeTags.push({ name: tag, edgeID });
  }
}

const graph = {
  datums,
  edges,
  datumTags,
  datumDimensions,
  datumTagAssociations: [],
  edgeTags,
  version: 0,
};

writeFileSync(outPath, JSON.stringify(graph, null, 2) + "\n");
console.log(
  `Wrote ${outPath}: ${datums.length} datums, ${edges.length} edges, ${datumTags.length} datum tags, ${edgeTags.length} edge tags, ${datumDimensions.length} dimension values.`,
);
